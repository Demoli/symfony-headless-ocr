declare(strict_types=1);

namespace App\Message;

use Symfony\Component\Uid\Uuid;

class OcrMessage
{
    protected $id;

    protected $file;

    /**
     * @return mixed
     */
    public function getId()
    {
        return $this->id;
    }

    /**
     * @param mixed $id
     */
    public function setId($id)
    {
        $this->id = $id;

        return $this;
    }

    public function generateId()
    {
        $this->setId(Uuid::v4());

        return $this;
    }

    /**
     * @return mixed
     */
    public function getFile()
    {
        return $this->file;
    }

    /**
     * @param mixed $file
     */
    public function setFile($file)
    {
        $this->file = $file;

        return $this;
    }


}
